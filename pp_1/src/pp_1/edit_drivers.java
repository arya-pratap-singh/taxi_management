    private void jList1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jList1MouseClicked
        // TODO add your handling code here:
        try{
            Class.forName("java.sql.Driver");
            Connection con =(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/taxi_booking","root","");
            Statement stmt = con.createStatement();
            String name = (String) jList1.getSelectedValue();
            String sql1 = "select * from drivers where name='"+(name)+"';";
            ResultSet rs = stmt.executeQuery(sql1);
            while(rs.next()){
                jTextField1.setText(rs.getString(2));
                jTextField2.setText(rs.getString(3));
                jTextField4.setText(rs.getString(4));
                jTextField5.setText(rs.getString(5));
               
                String ti = (String) rs.getString(6);
                
                //code to refersh jcombobox
              int Item = jComboBox1.getItemCount();
              if(Item>0)
                  {
                    for(int i=0;i<Item;i++)
                            {
                                jComboBox1.removeItemAt(0);
                            }
                  }
                
               
                Statement stmt1 = con.createStatement();
                String sq = "select model from taxi;";
                ResultSet rs1 = stmt1.executeQuery(sq);
                if(ti == null){
                jComboBox1.addItem("none");
                              jComboBox1.setSelectedItem("none");
                }
                
             while(rs1.next()){
              jComboBox1.addItem(rs1.getString("model"));
              
              
             
              Statement stmt2 = con.createStatement();
                String sql = "select model from taxi where taxi_id="+(ti)+";";
                ResultSet rs2 = stmt2.executeQuery(sql);
                while(rs2.next()){
                String vec =(String) rs2.getString("model");
                jComboBox1.setSelectedItem(vec);
                }
            
              }
                
            }
            
        
        }
        catch(Exception e){
        JOptionPane.showMessageDialog(this,e.getMessage());
        }
    }//GEN-LAST:event_jList1MouseClicked

