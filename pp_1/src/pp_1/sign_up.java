    private void jPanel3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel3MouseClicked
        // TODO add your handling code here:
        try{
               String name = jTextField1.getText();
              String number = jTextField5.getText();
              String password = jTextField4.getText();
              
              
              int pl = number.length();
              
              String regex = "^(?:(?:\\+|0{0,2})91(\\s*[\\-]\\s*)?|[0]?)?[789]\\d{9}$";
             
                Class.forName("java.sql.Driver");
                Connection con=DriverManager.getConnection("jdbc:mysql://localhost/taxi_booking","root","");
                Statement stmt = con.createStatement();
                
                if(name.isEmpty() || number.isEmpty() || password.isEmpty() ){
                if(name.isEmpty()){
                    jLabel4.setText("cannot be empty");
                    
                }
                if(number.isEmpty()){
                    jLabel6.setText("cannot be empty");
                    
                }
                if(password.isEmpty()){
                    jLabel5.setText("cannot be empty");
                    
                }
                
                
                }
                else if(!(number.matches(regex))){
                    jLabel6.setText("Incorrect format");
                    
                }
                else if(!(10 == pl)){
                    jLabel6.setText("10 digits required");
                    
                }
                else if("Username exist".equals(jLabel4.getText())){
                    JOptionPane.showMessageDialog(null,"User Already Exists!");
                }
                else{
                    String sq = "insert into user_cred(user_name,user_password,user_number) values('"+(name)+"','"+(password)+"','"+(number)+"');";
                    stmt.executeUpdate(sq);
                    new user_main(name).setVisible(true);
                    this.dispose();
                
                }
                
        }
        catch(Exception e){
                JOptionPane.showMessageDialog(this, e.getMessage());
            }
        
    }//GEN-LAST:event_jPanel3MouseClicked

