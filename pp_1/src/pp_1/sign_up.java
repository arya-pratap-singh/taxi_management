    private boolean validate_Sign_in(String username) {
         try{
            Class.forName("java.sql.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/taxi_booking","root","");
             PreparedStatement pst = con.prepareStatement("Select user_name from user_cred where user_name=?");
       pst.setString(1, username); 
       
       ResultSet rs = pst.executeQuery();                        
       if(rs.next()) {           
           String na = rs.getString("user_name");
           String nam = jTextField1.getText();
           if(na.equals(nam)){
           return true;
           }
       }
       else
           return false;            
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
            
        }
        return false;
        
        
  
}
