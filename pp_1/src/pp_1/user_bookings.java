    private void jPanel2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jPanel2MouseClicked
        // TODO add your handling code here:
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        int r = jTable1.getSelectedRow();
        try{
            Class.forName("java.sql.Driver");
            Connection con =(Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/taxi_booking","root","");
            Statement stmt = con.createStatement();
            String s = model.getValueAt(r, 0).toString();
            String sl = "delete from Bookings where s_no='"+(s)+"'";
            int i = JOptionPane.showConfirmDialog(null, "Do you want do delete Booking with S_no "+(s)+" ?");
            if(i == JOptionPane.YES_OPTION){
                stmt.executeUpdate(sl);
                jLabel2.setText("");
                jLabel3.setText("");
                jLabel4.setText("");
                jLabel8.setText("");
                jLabel12.setText("");
                while(model.getRowCount() > 0)
                           {
                              model.removeRow(0);
                           }
                
                try{
           
            Statement stmt1 = con.createStatement();
            String query = "select s_no,user_name,location,capacity,driver,prize from Bookings;";
            ResultSet rs = stmt.executeQuery(query);
            while(rs.next()){
                String s_no = rs.getString("s_no");
                String user = rs.getString("user_name");
                String loc = rs.getString("location");
                String capa = rs.getString("capacity");
                String driv = rs.getString("driver");
                String pri = rs.getString("prize");
                
                model.addRow(new Object[]{s_no,user,loc,capa,driv,pri});
            }
            rs.close();
            stmt.close();
            con.close();
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
                
                
                
                
                
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }//GEN-LAST:event_jPanel2MouseClicked

