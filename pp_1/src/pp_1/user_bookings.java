    public user_bookings() {
        initComponents();
        try{
            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            Class.forName("java.sql.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/taxi_booking","root","");
            Statement stmt = con.createStatement();
            String query = "select s_no,user_name,location,capacity,driver,prize from Bookings;";
            ResultSet rs = stmt.executeQuery(query);
            while(rs.next()){
                String s_no = rs.getString("s_no");
                String user = rs.getString("user_name");
                String loc = rs.getString("location");
                String capa = rs.getString("capacity");
                String driv = rs.getString("driver");
                String pri = rs.getString("prize");
                
                model.addRow(new Object[]{s_no,user,loc,capa,driv,pri});
            }
            rs.close();
            stmt.close();
            con.close();
            
            
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.getMessage());
        }
    }

